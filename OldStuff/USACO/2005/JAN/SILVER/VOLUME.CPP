#include <fstream>
#include <algorithm>
using namespace std;

#define problem "d"
#define MAXN 10001

typedef long long int64;

int N, i, ls[MAXN];
int64 sol;

ifstream fin ( problem ".in" );
ofstream fout ( problem ".out" );

int main() {

  fin >> N;
  for ( i = 0; i < N; i++ )
    fin >> ls[i];

  sort( ls, ls + N );

  for ( i = 1; i < N; i++ )
    sol += int64( ls[i] - ls[i - 1] ) * i * ( N - i );

  fout << sol * 2 << endl;

  return 0;
}

